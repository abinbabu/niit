package daytwo;

public class Emp1 {
	
	private String Name;
	private  int Salary;
	
	public Emp1(String n,int s)
	{
		this.Name=n;
		this.Salary=s;
		
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public int getSalary() {
		return Salary;
	}

	public void setSalary(int salary) {
		Salary = salary;
	}
	
	

}
